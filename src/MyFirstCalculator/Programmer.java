    private void ButtonEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEqualActionPerformed
        String Result;
        SecondNum =Integer.parseInt(Display.getText());
        if (Operation == "+"){
            Display.setText(String.valueOf(FirstNum+SecondNum));
        }
        else if (Operation == "-"){
            Display.setText(String.valueOf(FirstNum-SecondNum));
        }
        else if (Operation == "*"){
            Display.setText(String.valueOf(FirstNum*SecondNum));
        }
        else if (Operation == "/"){
            Display.setText(String.valueOf(FirstNum/SecondNum));
        }
        else if (Operation =="&"){
            Display.setText(String.valueOf(FirstNum&SecondNum));
        }
        else if (Operation =="|"){
            Display.setText(String.valueOf(FirstNum|SecondNum));
        }
        else if (Operation =="~"){
            Display.setText(String.valueOf(~FirstNum));
        }
        else if (Operation =="X|"){
            Display.setText(String.valueOf(FirstNum^SecondNum));
        }
        else if (Operation =="!&"){
            Display.setText(String.valueOf(~(FirstNum&SecondNum)));
        }
        else if (Operation =="!|"){
            Display.setText(String.valueOf(~(FirstNum|SecondNum)));
        }
    }//GEN-LAST:event_ButtonEqualActionPerformed

