    private void DecEquation(){
        int Dec = Integer.parseInt(Display.getText());
        DecDisplay.setText(String.valueOf(Dec));
        
        String HexNumber = Display.getText();
        HexDisplay.setText((Integer.toHexString(Dec)).toUpperCase());
        
        String Bin = Integer.toBinaryString(Integer.parseInt(HexNumber));
        BinDisplay.setText(Bin);

        String Oct = Integer.toOctalString(Integer.parseInt(HexNumber));
        OctDisplay.setText(Oct);
    }
    private void HexEquation(){
        String HexNumber=Display.getText();
        HexDisplay.setText(String.valueOf(HexNumber));
        
        String Dec=String.valueOf(Integer.parseInt(HexNumber,16));
        DecDisplay.setText(Dec);
        
        String Bin = Integer.toBinaryString(Integer.parseInt(Dec));
        BinDisplay.setText(Bin);
        
        String Oct = Integer.toOctalString(Integer.parseInt(Dec));
        OctDisplay.setText(Oct);
    }
    private void OctEquation(){
        String Oct = Display.getText();
        OctDisplay.setText(Oct);
        
        int Dec=Integer.parseInt(Oct,8); 
        DecDisplay.setText(String.valueOf(Dec));
        
        String Hex = Integer.toHexString(Integer.parseInt(Oct, 8));
        HexDisplay.setText(Hex);
        
        String Bin = Integer.toBinaryString(Dec);
        BinDisplay.setText(Bin);
    }
